class Solution { 
public:
    
    int maxPathSum(TreeNode *root) {
        auto temp_res = DFS(root);
        return max_res;
    }
    int DFS(TreeNode *root) {
        int res = 0;
        if(!root) {
            return res;
        }
       // if((!root->left)&&(!root->right)) {
       //     res = root->val;
       // }
        res+= root->val;
        // 遍历左右的最大值
        int left_max = 0;
        int right_max = 0;
        if(root->left) {
            left_max = DFS(root->left);
        }
        if(root->right) {
            right_max = DFS(root->right);
        }
        if(left_max>=0) {
            res+=left_max;
        }
        if(right_max>=0) {
            res+=right_max;
        }
        max_res = max(res,max_res);
        return res;
    }
    int max_res= INT_MIN;
};

